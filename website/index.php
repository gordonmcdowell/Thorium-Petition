<div class="text">
Thorium, converted into energy in a Liquid Fluoride Thorium Reactor (LFTR pronounced "Lifter"), has the potential to completely eliminate our dependence on fossil fuel based electricity generation... All the while releasing orders of magnitude <a href="http://www.youtube.com/watch?v=Q3EGOL4J6yI"><strong>less radioactive material</strong> into the atmosphere than coal and natural gas</a>! <strong>No <a href="http://www.youtube.com/watch?v=Vbx_gFT0v7k">greenhouse gasses</a></strong> are emitted. And <a href="http://www.youtube.com/watch?v=BOoBTufkEog"><strong>operating costs</strong> are projected to easily <strong>compete with coal</strong></a>.
</div>

<div class="space">&nbsp;</div>

<div class="text">
<strong>LFTR</strong> has <strong>staggering</strong> implications beyond lowering the cost of electricity:
<ul>
<li>Separating thorium from heavy rare earths gives us access to heavy rare Earth elements <a href="http://www.nytimes.com/2012/01/31/business/wto-orders-china-to-stop-export-taxes-on-minerals.html">currently monopolized by China</a>, essential for <strong>hi-tech manufacturing jobs</strong>.<br /><br /></li>
<li>Normal LFTR operation <a href="http://www.youtube.com/watch?v=2at8C8YrX80">produces medical isotopes</a> (expected to be in short supply) for <strong>fighting Cancer</strong>, in an easy to partition form.<br /><br /></li>
<li>LFTR <a href="http://www.youtube.com/watch?v=tdusXIvyLFQ">produces fuel</a> (which the world is completely out of) <strong>needed by NASA</strong> to power deep space probes.<br /><br /></li>
<li>And the waste stream contains far, <strong>far less</strong> of "<a href="http://www.youtube.com/watch?v=Q3EGOL4J6yI">the bad stuff</a>" we've all come to associate with today's nuclear power: <strong>transuranics such as plutonium and curium</strong>.<br /></li>
</ul>
<strong>LFTR is  nuclear power, <em>without the drawbacks</em> of nuclear power.</strong>
</div>

