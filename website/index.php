<div class="text">
LFTR technology was explored at Oak Ridge National Laboratory in Tennessee, until research was halted by the Nixon administraton in 1974. This was a <a href="http://www.youtube.com/watch?v=QIkqbxYdadg"><strong>political</strong> decision</a> in favor of fast-breeder reactors, which have since never achieved commercial success. The case for LFTR (a type of reactor broadly referred to as "Thorium Molten Salt Reactor") is so <strong>compelling</strong>, we need to put the Obama administration on record: <strong>Exactly why</strong> are we not actively pursuing this technology?</div>

<div class="space">&nbsp;</div>

<div class="text">
  In <a href="http://nucleargreen.blogspot.com/2009/05/secretary-chus-answer-and-facts.html">2009 Secretary of Energy Steven Chu stated</a> LFTR is not part of the Office of Nuclear Energy's research program.
<ul>
<li>Chu cites corrosive effect of molten salt<br />...to which <a href="http://seekerblog.com/2010/01/02/wired-magazine-the-new-nuke-%E2%80%94-is-corrosion-a-problem/">ORNL noted in 1979</a> that <strong>Hastelloy N in conjunction with careful control of fuel salt's oxidation state appears promising, but extensive testing under reactor conditions are required.</strong> (As would have happened if research had not been halted.) <br /><br /></li>
<li>Chu cites high melting temperature of the salt<br />...which is one reason <strong>why LFTR is so efficient &amp; safe!</strong> The <a href="http://energyfromthorium.com/faq/">salt's properties do not change</a> even under intense radiation, unlike all solid forms of nuclear fuel.<br /><br /></li>
<li>Chu cites proliferation concerns<br />...ignoring the <a href="http://energyfromthorium.com/ieer-rebuttal/">plethora of reasons</a> weaponization would fail. <a "http://energyfromthorium.com/2010/10/02/lftr-discourages-weapons-proliferation/"><strong>LFTR can not be used to create weapons material</strong></a>.<br /></li>
</ul>
<p><a href="http://www.reddit.com/r/energy/comments/q349j/on_jan_13_i_sent_doe_sec_stephen_chu_an_email_in/">In a letter to a Redditor in 2011</a> Chu says they are currently funding a study   at Argonne National Laboratory (ANL) to evaluate the transition of a   uranium-fueled fast reactor to a solid fuel thorium fast reactor <em><strong>(not LFTR)</strong></em>, again citing proliferation concerns and now &quot;additional technical challenges&quot;.</p>
<p>&nbsp;</p>
<p>We've got your "additional technical challenges" <a href="http://www.members.shaw.ca/rlongpre01/moon.html">right here</a>, buddy!</p>
<p>&nbsp;</p>
<p>Steven Chu <strong>knows what a Thorium Molten-Salt Reactor is</strong>. But for whatever reason looks to &quot;evaluate&quot; thorium in a solid fuel reactor. <a href="http://www.youtube.com/watch?v=gbCW1-PVteU">The benefits of Thorium over Uranium in a solid fuel reactor are vastly   reduced</a>: Smaller capital costs. Smaller operating costs. Increased safety. Valuable medical isotope harvesting. <strong>Gone.</strong></p>
<p>&nbsp;</p>
<p>ANL test results of <strong>solid</strong> thorium fuel surely will be <em>unimpressive</em>. The <strong>key</strong> to <strong>unlocking thorium's full potential</strong> is consumption in a <strong>Molten-Salt Reactor</strong> such as <strong>LFTR</strong>.</p>
<p>&nbsp;</p>
<p>Thorium fuel cycle development must be continued in a <strong>liquid fuel reactor</strong> for such effort to be taken seriously by thorium advocates.</p>
